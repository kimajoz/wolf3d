#include "wolf3d.h"

void		w_gun_fire(t_wind *w)
{
	w->w.dist = 0.2;
	w->w.tmpgun = 1;
	w_set_pxl_spr((w->width / 2) + (GUNW * 2), w->height - GUNH * 2, GUNH * 2, w);
	w->w.tmpgun = 0;
}
