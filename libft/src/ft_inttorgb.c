int			*ft_inttorgb(int color)
{
	int		*col;

	col = 0;
	if (color == 256 * 256 * 256 + 256 * 256 + 256)
		return (0);
	col[0] = color % 256;
	col[1] = (color / 256) % 256;
	col[2] = (color / 256 / 256) % 256;
	return (col);
}
