#include "../inc/libft.h"

int					ft_nbrlen(int n)
{
	int				i;

	i = 0;
	if (n == 0)
		i++;
	else if (n < 0)
		return (ft_nbrlen(-n) + 1);
	else
	{
		while (n > 0)
		{
			i++;
			n /= 10;
		}
	}
	return (i);
}
