#include "../inc/libft.h"

int			ft_isnumber(char *str)
{
	int		c;
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_isdigit(ft_atoi(str));
		i++;
	}
	c = str[i];
	if (c >= '0' && c <= '9')
		return (1);
	return (0);
}
